public class LoopThroughArray {
  public static void main(String[] args) {
    // loop through an integer array and print out each element
    int[] arr = {1,2,3,4,5};
    for (int i = 0; i < arr.length; i++) {
      System.out.println(arr[i]);
    }
  }
}
